'''Comparison Operators'''

'''
Op              Meaning                             Example
>               Greater                             10>8
>=              Greather than or equal to           10>=10
<               Lesser                              8<10
<=              Lesser than or equal to             8<=8
==              equal to                            8==8
!=              diferent                            8!=10
'''

greater = 2>1
lesser = 1<2
equal = 'a' == 'a'
diferent = 'a' != 'b'
print(greater,lesser,equal,diferent,sep='\n')
